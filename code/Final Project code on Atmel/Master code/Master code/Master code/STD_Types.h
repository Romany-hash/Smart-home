/*
 * STD_Types.h
 *
 * @file STD_Types.h
 * @brief Standard data types for microcontroller programming.
 * @details This file defines standard data types used in microcontroller programming, such as
 *          unsigned integers of various sizes, signed integers of various sizes, floating-point
 *          types, and unsigned long integers.
 *	@author Romany Sobhy
*/

#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char  uint8;
typedef signed char    sint8;
typedef unsigned short uint16;
typedef signed short   sint16;
typedef double         float64;
typedef unsigned long  uint32;

#endif /* STD_TYPES_H_ */
